    private void loadCourseInstructor() throws SQLException {
        List list = cibll.loadCourseInstructor(1);
        DefaultTableModel model = convertCourseInstructor(list);
        tblPhanCong.setModel(model);
    }
    
    private DefaultTableModel convertCourseInstructor(List list) {
        Object[][] data = new Object[list.size()][4];
        String colNames[] = {"ID Course", "Course Title", "ID Person", "Name"};
        for(int i= 0 ; i < list.size(); i++) {
            DTO_CourseInstructor obj = (DTO_CourseInstructor) list.get(i);
            data[i][0] = obj.getCourseID();
            data[i][1] = obj.getCourseTitle();
            data[i][2] = obj.getPersonID();
            data[i][3] = obj.getPersonName();
        }
        DefaultTableModel model = new DefaultTableModel(data, colNames) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
            
        };
        return model;
    }
